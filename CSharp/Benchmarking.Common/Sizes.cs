namespace Benchmarking.Common
{
    public static class Sizes
    {
        public const double KB = 1024;
        public const double MB = KB * KB;
        public const double GB = MB * KB;
        public const double TB = GB * KB;
        
        public const double Kilo = 1000; 
        public const double Mega = Kilo * Kilo; 
        public const double Giga = Mega * Kilo; 
        public const double Tera = Giga * Kilo;
        
        public const double Milli = 0.001;
        public const double Micro = Milli * Milli;
        public const double Nano = Micro * Milli;
        public const double Pico = Nano * Milli;
    }
}
